void InitDispData(void)
{
    int i;

    /*Need to flip the bits in the bitmaps to make them black text on gray background.*/

    for (i = 0; i < sizeof(welcomeScreen); i++)
    {
        welcomeScreen[i] = ~welcomeScreen[i];
        mainScreen[i] = ~mainScreen[i];
        menuScreen[i] = ~menuScreen[i];
        subMenuScreen[i] = ~subMenuScreen[i];
    }

    currentScreen = ds_BLANK;
}  

void SetScreenData(TE_DISPLAY_SCREENS screen)
{
    currentScreen = screen;
}

uint8_t* GetCurrentScreenData(void)
{
    switch (currentScreen)
    {
    case ds_WELCOME:
        return welcomeScreen;
    case ds_MAIN:
        return mainScreen;
    case ds_MAIN_MENU:
        return menuScreen;
    case ds_SUB_MENU:
        return subMenuScreen;
    default:
        return blankScreen;
    } 
}
